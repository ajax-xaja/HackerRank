import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int a = 0;
        while(scanner.hasNextLine()) {
            a++;
            System.out.println(a + " " + scanner.nextLine());
        }
    }
}

